/**
 * Traduit un texte en utilisant le dictionnaire culinaire
 * @param text - Texte à traduire
 * @returns Texte traduit ou null si pas dans le dictionnaire
 */
export function translateWithDictionary(text: string): string | null {
  const lowerText = text.toLowerCase().trim();
  return CULINARY_DICTIONARY[lowerText] || null;
}

/**
 * Traduit un texte en remplaçant les mots connus du dictionnaire
 * @param text - Texte à traduire
 * @returns Texte avec les mots traduits
 */
export function translateWordsInText(text: string): string {
  let result = text;
  
  // Trier les clés par longueur décroissante pour éviter les remplacements partiels
  const sortedKeys = Object.keys(CULINARY_DICTIONARY).sort((a, b) => b.length - a.length);
  
  for (const englishWord of sortedKeys) {
    const frenchWord = CULINARY_DICTIONARY[englishWord];
    // Échapper les caractères spéciaux dans la clé
    const escapedKey = englishWord.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    // Utiliser une regex avec des limites de mots pour éviter les remplacements partiels
    const regex = new RegExp(`\\b${escapedKey}\\b`, 'gi');
    result = result.replace(regex, frenchWord);
  }
  
  return result;
}

/**
 * Traduit agressivement un texte en utilisant UNIQUEMENT le dictionnaire
 * Cette fonction est plus fiable que l'API pour les termes culinaires
 * @param text - Texte à traduire
 * @returns Texte traduit sans API externe
 */
export function aggressiveTranslate(text: string): string {
  // D'abord vérifier si c'est une traduction directe
  const directTranslation = translateWithDictionary(text);
  if (directTranslation) {
    return directTranslation;
  }
  
  // Sinon, traduire mot par mot
  return translateWordsInText(text);
}
